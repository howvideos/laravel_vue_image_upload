    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'description' => 'required',
            'image' => 'required|image|mimes:jpeg,jpg,png|max:2048'
        ]);
        if ($request->file('image')) {
            $image = $request->file('image');
            $imageName = date('mdYHis'). uniqid();
            $desinationPath = public_path(). '/images';
            $image->move($desinationPath, $imageName);
        }
        $newImage = Gallery::create(
            [
                'name' => $request->name,
                'description' => $request->description,
                'image' => env('APP_URL'). 'images/'. $imageName
            ]
        );
        return response()->json(['message' => 'Image has been sucessfully saved', 'data'=> $newImage]);
    }

